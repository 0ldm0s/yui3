<h3>Creating A Custom Skin</h3>
<p>
Skinning the menus created using the MenuNav Node Plugin is done using CSS.  The stylesheet used 
for other MenuNav Node Plugin examples is a minified version of the menu-core.css and menu-skin.css 
files.  The menu-core.css file includes foundational styling that controls basic layout and 
positioning, whereas the menu-skin.css file is used to apply colors, borders, padding, etc. 
</p>

<p>
Skinning can be accomplished by either overriding the styles defined in the menu-skin.css file, or 
by creating an entirely new skin file.  In either case place custom styles in a separate file to 
simplify integrating with YUI updates.
</p>

<p>
When creating a custom skin, use the menu-core.css and menu-skin.css files as a reference.
Also consult the <a href="../../nodemenunav#css-reference">CSS reference</a> 
section of the MenuNav Node Plugin landing page, as it provides a complete list of all of the class 
names used by the MenuNav Node Plugin along with when and to what element(s) they are applied.
</p>